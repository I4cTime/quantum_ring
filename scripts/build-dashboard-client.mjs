#!/usr/bin/env node
/**
 * Bundles the status-dashboard browser client (Preact + htm + the app) into a
 * single minified IIFE and writes it to `src/core/dashboard-client.ts` as a
 * string constant.
 *
 * Why a generated string instead of a CDN or runtime file read:
 * the dashboard is served as one self-contained, offline, CSP-clean HTML page,
 * and the published package ships only `dist/`. Bundling the client here also
 * sidesteps a template-literal collision — htm uses `html`...${x}`` syntax,
 * which can't live inside the `getDashboardHtml()` TS template literal.
 *
 * The generated file is committed so `pnpm run build` works without a separate
 * bundling step in consumers; it is regenerated automatically by `build`.
 */
import { build } from "esbuild";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const entry = join(root, "src", "dashboard-client", "main.js");
const outPath = join(root, "src", "core", "dashboard-client.ts");
const nm = join(root, "node_modules");

const result = await build({
  entryPoints: [entry],
  bundle: true,
  minify: true,
  format: "iife",
  platform: "browser",
  target: ["es2019"],
  write: false,
  legalComments: "none",
});

const code = result.outputFiles[0].text.trim();

const htmPkg = JSON.parse(readFileSync(join(nm, "htm", "package.json"), "utf8"));
const preactPkg = JSON.parse(readFileSync(join(nm, "preact", "package.json"), "utf8"));

const banner =
  `// AUTO-GENERATED by scripts/build-dashboard-client.mjs — do not edit by hand.\n` +
  `// Bundled status-dashboard client: Preact ${preactPkg.version} + htm ${htmPkg.version} + app.\n` +
  `// Source: src/dashboard-client/main.js. Regenerate with: pnpm run build:dashboard\n\n`;

const body = `export const DASHBOARD_CLIENT = ${JSON.stringify(code)};\n`;

writeFileSync(outPath, banner + body, "utf8");
console.log(
  `Wrote ${outPath} (${(code.length / 1024).toFixed(1)} KB minified, preact@${preactPkg.version} htm@${htmPkg.version})`,
);
